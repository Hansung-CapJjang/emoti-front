                              const SizedBox(width: 5),
                              const Text("x 0", style: TextStyle(fontSize: 18, fontFamily: 'DungGeunMo')),
                            ],
                          ),
                          const SizedBox(width: 20),
                          Row(
                            children: [
                              Image.asset("assets/images/stamp5.png", width: 60),
                              const SizedBox(width: 5),
                              const Text("x 0", style: TextStyle(fontSize: 18, fontFamily: 'DungGeunMo')),
